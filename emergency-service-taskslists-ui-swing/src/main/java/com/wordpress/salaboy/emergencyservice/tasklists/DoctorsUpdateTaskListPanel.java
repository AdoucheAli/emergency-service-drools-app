    @Override
    public void refresh() {

        
        try {
            taskAbstracts = this.parent.getTaskClient().getMyTaskAbstracts("", "doctor", "", null, "", "", "", 0, 0);
        } catch (IllegalArgumentFault ex) {
            Logger.getLogger(DoctorsUpdateTaskListPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IllegalStateFault ex) {
            Logger.getLogger(DoctorsUpdateTaskListPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        DefaultTableModel tableModel = ((DefaultTableModel)doctorsUpdateTasksJTable.getModel());
        
        int rowCount = tableModel.getRowCount();
        for (int i = 0; i < rowCount; i++) {
            tableModel.removeRow(0);
        }
        
        int i = 0;
        for (TTaskAbstract taskAbstract : taskAbstracts) {
            String name = taskAbstract.getName().toString();
            tableModel.addRow(new Object[]{++i,name});
        }
        
        doctorsUpdateTasksJTable.getColumnModel().getColumn(0).setMaxWidth(25);
        
    }

