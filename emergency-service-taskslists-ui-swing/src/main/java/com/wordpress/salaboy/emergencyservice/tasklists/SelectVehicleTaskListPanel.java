    public void refresh() {


        try {
            taskAbstracts = this.parent.getTaskClient().getMyTaskAbstracts("", "garage_emergency_service", "", null, "", "", "", 0, 0);
        } catch (IllegalArgumentFault ex) {
            Logger.getLogger(SelectVehicleTaskListPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IllegalStateFault ex) {
            Logger.getLogger(SelectVehicleTaskListPanel.class.getName()).log(Level.SEVERE, null, ex);
        }

       
        DefaultTableModel tableModel = ((DefaultTableModel) selectVehicleJTable.getModel());

        int rowCount = tableModel.getRowCount();
        for (int i = 0; i < rowCount; i++) {
            tableModel.removeRow(0);
        }

        int i = 0;
        for (TTaskAbstract taskAbstract : taskAbstracts) {
            String name = taskAbstract.getName().toString();
            tableModel.addRow(new Object[]{++i, name});
        }
        
        selectVehicleJTable.getColumnModel().getColumn(0).setMaxWidth(25);
    }
    private JDialog callPopup;

    public void selectVehicleTaskSelected(String id) {
        SelectAmbulanceTaskFormPanel selectAmbulanceTaskForm = new SelectAmbulanceTaskFormPanel(this);
        callPopup = new JDialog(this.parent, "Info", true);
        callPopup.add(selectAmbulanceTaskForm);
        this.callPopup.setSize(320, 470);
        this.callPopup.setVisible(true);
        this.callPopup.requestFocus();
    }


    
